void print_network(void)
{
    puts("\nsphinx network nodes:");
    for (uint8_t i=0; i<SPHINX_NET_SIZE; i++) {
        ipv6_addr_print(&network_pki[i].addr);
        puts("");
    }
    puts("");
}

network_node *get_node(ipv6_addr_t *node_addr)
{   
    for (uint8_t i=0; i < SPHINX_NET_SIZE; i++) {
        if (ipv6_addr_equal(&network_pki[i].addr, node_addr)) {
            return (network_node*) &network_pki[i];
        }
    }

    /* nothing found */
    return NULL;
}

int8_t build_mix_path(network_node *path_nodes[], uint8_t path_len, ipv6_addr_t *start_addr, ipv6_addr_t *dest_addr)
{
    uint32_t random;
    char chosen[SPHINX_NET_SIZE] = {0};

    /* select random mix nodes */
    uint8_t i = 0;
    while (i < (path_len-1)) {
        random = random_uint32_range(0, SPHINX_NET_SIZE);

        if (chosen[random]) {
            continue;
        }

        if (ipv6_addr_equal(&network_pki[random].addr, start_addr) || ipv6_addr_equal(&network_pki[random].addr, dest_addr)) {
            chosen[random] = '1';
            continue;
        }

        path_nodes[i] = (network_node *) &network_pki[random];
        chosen[random] = '1';
        i++;
    }

    /* add final destination node */
    if ((path_nodes[i] = get_node(dest_addr)) == NULL) {
        puts("sphinx: error: destination address not found in pki");
        return -1;
    }

    return 1;
}

ipv6_addr_t pki_get_random_addr(void)
{
    uint32_t random;
    
    while(1) {
        random = random_uint32_range(0, SPHINX_NET_SIZE);
        if (ipv6_addr_equal(&network_pki[random].addr,&local_addr)) {
            continue;
        }
        return network_pki[random].addr;
    }
}